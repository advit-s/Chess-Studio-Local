import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import ts from 'typescript';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const generatedModules = [
  {
    source: path.join('src', 'lib', 'boardDetection.ts'),
    output: path.join('public', 'ocr-core.js'),
  },
  {
    source: path.join('src', 'lib', 'ocrModelContract.ts'),
    output: path.join('public', 'ocr-model-contract.js'),
  },
  {
    source: path.join('src', 'lib', 'ocrWorkerState.ts'),
    output: path.join('public', 'ocr-worker-state.js'),
  },
];

const host = {
  getCanonicalFileName: (fileName) => fileName,
  getCurrentDirectory: () => root,
  getNewLine: () => '\n',
};
const checkOnly = process.argv.includes('--check');

for (const generatedModule of generatedModules) {
  const sourcePath = path.join(root, generatedModule.source);
  const outputPath = path.join(root, generatedModule.output);
  const source = await readFile(sourcePath, 'utf8');
  const compiled = ts.transpileModule(source, {
    compilerOptions: {
      module: ts.ModuleKind.ES2022,
      target: ts.ScriptTarget.ES2022,
      removeComments: false,
    },
    fileName: sourcePath,
    reportDiagnostics: true,
  });

  if (compiled.diagnostics?.length) {
    throw new Error(ts.formatDiagnostics(compiled.diagnostics, host));
  }

  const banner = `// Generated from ${generatedModule.source}. Do not edit this file directly.\n`;
  const expected = banner + compiled.outputText;
  if (checkOnly) {
    const current = await readFile(outputPath, 'utf8').catch(() => '');
    if (current !== expected) {
      throw new Error(`${generatedModule.output} is stale. Run npm run build:ocr-core.`);
    }
  } else {
    await writeFile(outputPath, expected);
  }
}

console.log(checkOnly
  ? 'Verified generated OCR modules are current.'
  : 'Generated production OCR modules from their typed sources.');
